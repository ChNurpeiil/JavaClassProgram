package homework.java.homework019;

public class HomeWork1 {
//    1. Write a program to print the sum
//    of even numbers from 1 to 10

    public static void main(String[] args) {

        for (int i = 1; i <= 10; i++){
            if (i % 2==0){
                System.out.println(i);
            }
        }
    }

}
