package com.java.class19;
//1 + 2 + 3 + 4 + 5
public class Sum0f1to5 {
    public static void main(String[] args) {
        int sum = 0;
        for (int i = 1; i <= 5; i++){
            sum = sum + i;
        }

        System.out.println(sum);


        //1 * 2 * 3 * 4 * 5

        int sum1 = 1;
        for (int j = 1; j <= 5; j++){
            sum1= sum1 * j;

        } System.out.println(sum1);

    }
}
